package de.itemis.jee7.util;

/**
 * This interface defines three different resource bundle identifiers.
 * @author sm
 *
 */
public interface ResourceBundleDefinitions
{
	/**
	 * The definition of regular messages.
	 */
	public final static String BUNDLE_MESSAGES = "msg";

	/**
	 * Ths definition of error messages.
	 */
	public final static String BUNDLE_ERRORS   = "err";
	
	/**
	 * The definition of version information.
	 */
	public final static String BUNDLE_VERSION  = "ver";
}
